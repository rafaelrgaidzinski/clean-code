def area_q(lado):
    return lado * lado

def area_r(base, altura):
    return base * altura

def area_t(base, altura):
    return (base * altura) / 2

# Propósito:
# Calcular áreas de figuras geométricas.
#
# Como funciona:
# 1. area_q → calcula área do quadrado (lado * lado).
# 2. area_r → calcula área do retângulo (base * altura).
# 3. area_t → calcula área do triângulo ((base * altura)/2).